# Suma, resta, multiplicación, división
a <- 10
b <- 5

suma <- a + b
resta <- a - b
multiplicacion <- a * b
division <- a / b
potencia <- a ^ b
modulo <- a %% b  # residuo de la división

print(suma)
print(resta)
print(multiplicacion)
print(division)
print(potencia)
print(modulo)

# Operaciones con vectores
x <- c(1, 2, 3, 4, 5)
y <- c(5, 4, 3, 2, 1)

print(x + y)  # suma elemento a elemento
print(x * y)  # multiplicación elemento a elemento